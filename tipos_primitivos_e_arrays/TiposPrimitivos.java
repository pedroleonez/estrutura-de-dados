package tipos_primitivos_e_arrays;

public class TiposPrimitivos {
    public static void main(String[] args) {
        int inteiro = 10; // 32 bits
        double decimal = 4.6; // 64 bits
        float flutuante = 3.8f; // 32 bits
        long longo = 88888L; // inteiro longo - 64 bits
        short curto = 55; // inteiro curto - 16 bits
        byte bite = 4; // inteiro pequeno - 8 bits
        char caractere = 'P';
        boolean ok = true;

        System.out.println(inteiro);
        System.out.println(decimal);
        System.out.println(flutuante);
        System.out.println(longo);
        System.out.println(curto);
        System.out.println(bite);
        System.out.println(caractere);
        System.out.println(ok);
    }
}
